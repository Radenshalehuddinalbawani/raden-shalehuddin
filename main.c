#include <stdio.h>

int main()
{
	// Menampilkan Nilai bilangan integer
	printf("\nNo                : %d", 134); 
	// Menampilkan Nilai bilangan real
	printf("\nNilai             : %f", 10.65);
	// Menampilkan string
	printf("\nNama              : %s", "Bang Toyib");
	// Menampilkan karakter
	printf("\nNilai Anda adalah : %c", 'A');
}
